package day01;/*
标识符：凡是代码中需要自己“命名”的地方都是标识符。
	   给变量、类、方法等命名的字符序列就是标识符。

标识符有命名规则：（必须遵守，又称为硬规则，不遵守编译会报错，或者运行出问题）
（1）只能使用26个英文字母大小写，数字0-9，下划线_，美元符号$
（2）数字不能开头
（3）不能直接使用关键字、保留字、特殊值	   
（4）严格区分大小写
（5）不能包含空格

标识符有命名规范：（建议遵守，又称为软规则，潜规则，不遵守不报错，但是会被鄙视，排外）
（1）见名知意
例如：存储年龄，可以用age
（2）类名：每一个单词的首字母大写，例如：HelloWorld
形式：XxxYyyZzz
（3）变量名、方法名：从第二个单词开始首字母大写，例如：main
形式：xxxYyyZzz
（4）包名：所有单词都小写，单词之间使用.分割
形式：xxx.yyy.zzz
（5）常量名：所有单词都大写，单词之间使用下划线_分割
形式：XXX_YYY_ZZZ
*/
